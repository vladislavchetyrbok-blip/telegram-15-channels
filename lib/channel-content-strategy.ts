export function getChannelRubric(channelId: string, index: number) {
  const profile = profiles[channelId] ?? profiles["money-opportunities"];
  return profile.rubrics[index % profile.rubrics.length];
}

export function generateChannelPostContent(channelId: string, topic: string): GeneratedChannelPost {
  const profile = profiles[channelId] ?? profiles["money-opportunities"];
  const body = [
    profile.intro(topic),
    ...profile.points(topic).map((point) => `- ${point}`),
    profile.conclusion(topic),
    profile.cta,
  ].join("\n\n");

  return {
    title: profile.title(topic),
    body,
    rubric: profile.rubricLabel,
    visualMode: profile.visualMode,
  };
}

export function getChannelVisualBrief(channelId: string, topic: string) {
  const profile = profiles[channelId] ?? profiles["money-opportunities"];
  return {
    rubricLabel: profile.rubricLabel,
    visualMode: profile.visualMode,
    bullets: profile.points(topic).slice(0, 3).map((point) => point.replace(/[.:;]$/g, "")),
  };
}

export function findGenericContentIssues({ channelId, title, body, topic }: { channelId: string; title: string; body: string; topic: string }) {
  const issues: string[] = [];
  const combined = `\n`.toLowerCase();
  const genericFragments = [
    "начните с простой проверки",
    "важно не искать",
    "короткий чек-лист",
    "расходы и цены",
    "вывод: выберите один",
    "такой подход снижает",
  ];

  for (const fragment of genericFragments) {
    if (combined.includes(fragment)) issues.push(`generic_phrase:`);
  }

  if (/(rub|руб|₽|росси|россий)/i.test(combined)) {
    issues.push("forbidden_currency_rub");
  }

  if (!combined.includes(topic.toLowerCase())) issues.push("topic_not_reflected_in_text");

  return issues;
}

