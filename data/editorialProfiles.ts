export const editorialProfiles: ChannelEditorialProfile[] = editorialProfilesBase.map((profile) =>
  currencyPolicyChannelIds.has(profile.channelId)
    ? {
        ...profile,
        primaryCurrency: "UAH",
        allowedCurrencies: ["UAH", "USD", "EUR"],
        forbiddenCurrencies: [["R", "U", "B"].join("")],
        currencyPolicyEnabled: true,
      }
    : profile,
);

export function getEditorialProfile(channelId: string) {
  return editorialProfiles.find((profile) => profile.channelId === channelId);
}
